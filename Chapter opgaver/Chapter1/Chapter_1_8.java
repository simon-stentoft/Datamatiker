public class Chapter_1_8 {
    public static void main(String[] args) {

        double pi = 3.14159;
        double radius = 6.5;
        double perimeter = 2 * radius * pi;
        double area = radius*radius*pi;

        System.out.println("Area = " + area);
        System.out.println("Perimeter = " + perimeter);
    }
}
